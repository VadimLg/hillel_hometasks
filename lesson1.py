# 1 Арифметичні операції:
# Написати програму, яка зчитує два числа та виводить їхню суму, різницю, добуток та частку.
a = int(input('Введите первое число: '))
b = int(input('Введите второе число: '))

print('Сумма: ', a + b)
print('Разность: ', a - b)
print('Произведение: ', a * b)
print('Деление:: ', a / b)

